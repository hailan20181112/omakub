#!/bin/bash
# 终端工具安装脚本 - 安装常用的终端工具

# 安装以下工具：
# fzf - 模糊查找工具
# ripgrep - 高性能文本搜索工具
# bat - 带有语法高亮的cat替代品
# eza - 现代ls替代品
# zoxide - 智能cd命令替代品
# plocate - 快速文件查找工具
# btop - 系统资源监视器
# apache2-utils - Apache工具集（包含ab基准测试工具）
# fd-find - 简单、快速的find替代品
# tldr - 简化版man页面
sudo apt install -y fzf ripgrep bat eza zoxide plocate btop apache2-utils fd-find tldr
